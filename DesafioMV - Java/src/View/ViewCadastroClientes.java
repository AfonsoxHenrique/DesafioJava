    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        MouseEvent e = evt;
        int index = jTable1.getSelectedRow();
        int click = e.getClickCount();
        if(jRadioButtonListarFisicas.isSelected() == false && jRadioButtonListarJuridicas.isSelected() == false)
        {
            JOptionPane.showMessageDialog(this, "Selecione o tipo de pessoa (PF/PJ)", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        if (jRadioButtonListarFisicas.isSelected() == true) {
            try {
                if (click == 2) {
                    if (index >= 0) {
                        ClientePF clientepf = this.respostaClientePF.get(index);
                        String mensagem = "Deseja remover este cliente?\nCliente: " + clientepf.getNome() + "\nCPF: " + clientepf.getCpf() + "\nTelefone: " + clientepf.getTelefone_PF();
                        int resultado = JOptionPane.showConfirmDialog(this, mensagem, "Remover cliente", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                        if (resultado == JOptionPane.YES_OPTION) {

                            NegocioClientePF negocioClientePF = new NegocioClientePF();
                            negocioClientePF.excluirClientePF(clientepf);
                            ListarCpfTable();
                            JOptionPane.showMessageDialog(this, "Cliente removido", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                        }
                    }
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Atenção", JOptionPane.WARNING_MESSAGE);
            }
        }
        if (jRadioButtonListarJuridicas.isSelected() == true) {
            try {
                if (click == 2) {
                    if (index >= 0) {
                        ClientePJ clientepj = this.respostaClientePJ.get(index);
                        String mensagem = "Deseja remover este cliente?\nCliente: " + clientepj.getRazaoSocial() + "\nCNPJ: " + clientepj.getCnpj() + "\nEmpresa: " + clientepj.getNomeEmpresa();
                        int resultado = JOptionPane.showConfirmDialog(this, mensagem, "Remover cliente", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                        if (resultado == JOptionPane.YES_OPTION) {

                            DadosClientePJ DadosClientePJ = new DadosClientePJ();
                            DadosClientePJ.excluirClientePJ(clientepj);
                            ListarCnpjTable();
                            JOptionPane.showMessageDialog(this, "Cliente removido", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                        }
                    }
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Atenção", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

