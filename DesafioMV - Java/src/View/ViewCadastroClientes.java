    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed

        if (jRadioButtonClientePF.isSelected() == true) {
            try {
                ClientePF clientepf = new ClientePF();
                SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                Date dataCadastro = new java.sql.Date(formato.parse(jTextFieldData.getText()).getTime());
                clientepf.setNome(jTextFieldNome.getText());
                clientepf.setCpf(jTextFieldCPF.getText());
                clientepf.setEndereco_PF(jTextFieldEndereco.getText());
                clientepf.setBairro_PF(jTextFieldBairro.getText());
                clientepf.setEstado_PF(jTextFieldEstado.getText());
                clientepf.setTelefone_PF(jTextFieldTelefone.getText());
                clientepf.setDataCadastro_PF(dataCadastro);
                clientepf.setCidade_PF(jTextFieldCidade.getText());

                NegocioClientePF negocioClientePF = new NegocioClientePF();
                negocioClientePF.cadastrarClientePF(clientepf);
                JOptionPane.showMessageDialog(this, "Cliente cadastrado", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                ApagandoTextFileds();
                ListarCpfTable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Aviso", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            if (jRadioButtonClientePJ.isSelected() == true) {
                try {
                    ClientePJ clientepj = new ClientePJ();
                    SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                    Date dataCadastro = new java.sql.Date(formato.parse(jTextFieldData.getText()).getTime());
                    clientepj.setRazaoSocial(jTextFieldNome.getText());
                    clientepj.setCnpj(jTextFieldCPF.getText());
                    clientepj.setEndereco_PJ(jTextFieldEndereco.getText());
                    clientepj.setBairro_PJ(jTextFieldBairro.getText());
                    clientepj.setTelefone_PJ(jTextFieldTelefone.getText());
                    clientepj.setDataCadastro_PJ(dataCadastro);
                    clientepj.setNomeEmpresa(jTextFieldNomeEmpresa.getText());
                    clientepj.setCidade_PJ(jTextFieldCidade.getText());
                    clientepj.setEstado_PJ(jTextFieldEstado.getText());

                    NegocioClientePJ negocioClientePJ = new NegocioClientePJ();
                    negocioClientePJ.cadastrarClientePJ(clientepj);
                    JOptionPane.showMessageDialog(this, "Cliente cadastrado", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                    ApagandoTextFileds();
                    ListarCnpjTable();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Aviso", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Escolha PJ ou PF", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

